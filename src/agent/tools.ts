// HR 行为面试问题内容
const BEHAVIOURAL_QUESTIONS = `# 行为面试技巧
HR 行为面试，考察工作态度和价值观。

## 个人介绍
使用如下模板，用自然舒缓的语气 1min 之内表达完，时间不要太长。
1. 个人履历，近几年的教育/工作经历
2. 擅长的业务领域（如医疗、3D、地图等），做过的主要项目
3. 擅长的技术栈 —— 程序员，最终还是要说到技术上

注意事项：
- 不要太短，不要两三句话说完，要尽量全面的表达出自己
- 不要太啰嗦，不要深入细节，对方可能听不懂（这才面试刚开始）
- 要有条理性，不要想一个说一个，逻辑很混乱

## 离职原因？
常规说法：想换一个更大的平台，继续提升自己的能力。
如被裁员：实话实说即可，现在这行情被裁员也很正常。

注意事项：
- 不要谈钱，不要说为了涨工资而离职
- 不要谈任何前公司的坏话，例如 xxx 项目/领导 不好而离职

## 空窗期为何这么久？
推荐表达：一开始对自己要求比较高，想找一个理想的工作，一直没有合适的机会。

注意事项：
- 不要说"学习了一段时间"，暴露自己技术能力不好
- 不要说"休息了一段时间"，我们都自愿加班，根本不需要休息
- 不要说"出去玩了"，老板和 HR 不喜欢爱玩的员工

## 你为何选择我们公司？
推荐表达方式：
- 提前熟悉公司背景、主营业务、JD 招聘信息、负责的项目
- 说出其中的 xxx 项目/技术 比较吸引你，或者 xxx 福利比较吸引你
- 再说出自己的 xxx 技能比较符合公司的招聘要求，所以就投了简历

## 你对这份工作有什么期许？
推荐表达方式：
- 希望通过这个工作，参与项目，团队分享等，能让自己在 xxx 技术领域，提升更高一个层级
- 自己技术提升了，才能更好的服务于公司的项目/产品，承担更重要的职责

## 你喜欢什么样的团队和领导？
推荐表达方式：
- 喜欢一个有技术氛围的团队，喜欢分享技术的领导，大家可以一起成长进步
- 技术氛围好，才能更好的保证代码质量和产品稳定性

## 未来 3-5 年的规划是什么？
参考：技术进步 + 承担更重要的职责 + 为公司项目服务

## 如何看待加班？
现在加班成了常态，认可加班的态度。
其实换位思考一下，如果我们也严格执行 8 小工作制，那你的工资还会这么高吗？

## 你的优点是什么？
推荐表达方式：
- 优先选择硬技能，自己擅长的技术领域，业务领域，能更好的用于工作中
- 如果实在找不到擅长的技术领域，可以说一些软技能：认真，准时，容易沟通，为人和善友好，工作有激情，英语好...

## 你的缺点是什么？
注意，这是个坑！正确的方式是：说缺点，也要说自己的补救措施，最终体现自己是一个爱学习、要求进步的人。
推荐的表达方式：
- 我觉得自己目前在 xxx 方面还有所欠缺
- 但我已经开始通过 xxx 学习这方面的知识了，计划 xxx 天以后即可完成

## 是否和前领导有过冲突？如何解决的？
使用 STAR 模型讲解：
- Situation 背景：记得在 xxx 项目中，我和领导有了不一致的意见
- Task 目标/任务：但我们都是为了同一个目标，能把项目尽快上线
- Action 行动/解决方案：找核心成员一起开会讨论，划分优先级，重新制定计划
- Result 结果：最后项目成功上线，且客户很满意

## 如果 PM 给你的项目加需求，你该如何应对？
常见解决方式：
- 先和自己领导沟通
- 如果领导同意，且自己评估工作量不大，那就加上
- 如果确定不接这个需求，让 PM 发邮件，走正式的需求变更流程

## 如果你项目的 deadline 很紧张，你该如何做？
使用 STAR 模型讲解：
- 和领导沟通，协同资源
- 和项目组开会，明确需求范围，划分优先级，制定开发计划
- 集中加班开发，即时沟通，即时调整

## 介绍一个你最近做过的项目
推荐的表达方式：
- 项目背景和功能介绍：这是一个 xxx 项目，它服务于 xxx
- 技术栈：说出 2-3 个主要的即可
- 个人的角色和成绩：我是核心开发人员，主要负责 xxx 模块
- 个人成长：通过开发这个项目，让我掌握了 xxx 能力

## 说一件最让你有成就感的事情
使用 STAR 模型讲解，最后总结为自己的能力提升。

## 说一件最让你感觉失败的事情
虽然做失败了，但让我学到了 xxx，最终还是要总结为自己能力的提升。

## 请用三个词概括自己
可选的词：计划性、执行力、热情、积极、创造性、极客精神、时间观念强、善于沟通、乐于分享、组织能力、管理能力、专注

## 你的同事/领导如何评价你？
例如：工作积极热情、善于团队合作、热爱技术。尽量举一个例子证明一下。

## 你业余有什么兴趣爱好？
说几个程序员常见的爱好即可，例如玩游戏、看电影、听歌等。
不要说太影响工作和加班的爱好，如徒步、骑行、长途旅行等。

## 如何主持一次会议？
综合考察你的沟通和组织能力：
- 明确会议主题、todos 和核心参与人
- 联系核心参与人，协同时间，确定会议时间
- 发送邮件给全体参会人员，抄送相关领导
- 制定会议议程，主持会议，做会议记录
- 会议结束，整理会议记录和决议，发送邮件`;

